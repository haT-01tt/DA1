    public void fillToTable() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblListCus.getModel();
        listCus = new CustomerServiceImpl().findAll();
        defaultTableModel.setRowCount(0);
        for (Customer x : listCus) {
            defaultTableModel.addRow(new Object[]{
                x.getId(), x.getFullName(), x.getPhoneNumber(),
                x.getAddress(), x.getCusCode(), x.getEmail(), x.getGender() ? "Male" : "Female"});
        }
    }

    public static String getSaltString() {
        String SALTCHARS = "0123456789";
        StringBuilder salt = new StringBuilder();
        Random rnd = new Random();
        while (salt.length() <= 1) { // length of the random string.
            int index = (int) (rnd.nextFloat() * SALTCHARS.length());
            salt.append(SALTCHARS.charAt(index));
        }
        String saltStr = salt.toString();
        return saltStr;

    }

