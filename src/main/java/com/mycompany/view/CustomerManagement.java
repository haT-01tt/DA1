    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtCusCode.getText().equals("") || txtFullName.getText().equals("") || txtAddress.getText().equals("") || txtEmail.getText().equals("") || txtPhoneNumber.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter the information");
            return;
        }
        customerService = new CustomerServiceImpl();

        try {
            String cusCode = txtCusCode.getText().trim();
            customer = customerService.getByCusCode(cusCode);
            if (customer != null) {

                customer.setFullName(txtFullName.getText());

                customer.setAddress(txtAddress.getText());
                customer.setEmail(txtEmail.getText());
                customer.setGender(rdoMale.isSelected());
                customer.setGender(!rdoFemale.isSelected());

                customerService.modify(customer);
                JOptionPane.showMessageDialog(this, "Update success");
                fillToTable();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Update fail");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtCusCode.getText().equals("") || txtFullName.getText().equals("") || txtAddress.getText().equals("") || txtEmail.getText().equals("") || txtPhoneNumber.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter the information");
            return;
        }
        try {
            Customer customer = new Customer();
            customer.setFullName(txtFullName.getText());
            customer.setPhoneNumber(txtPhoneNumber.getText());
            customer.setAddress(txtAddress.getText());
            customer.setEmail(txtEmail.getText());
            customer.setCusCode(txtCusCode.getText());
            customer.setGender(rdoMale.isSelected());
            customer.setGender(rdoFemale.isSelected());
            customer.setAvai(true);
            customerService = new CustomerServiceImpl();
            customerService.create(customer);

            JOptionPane.showMessageDialog(this, "Save success");
            fillToTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Save Fail");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        customerService = new CustomerServiceImpl();
        if (customer == null) {
            JOptionPane.showMessageDialog(this, "Choose customer pls");
        } else {
            Boolean bl = customerService.remove(customer.getId());
            if (bl) {
                JOptionPane.showMessageDialog(this, "Remove success");
                fillToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Remove Fail");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        customerService = new CustomerServiceImpl();
        if (txtCusCode.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please input Customer Code");
        } else {
            try {
                String cusCode = txtCusCode.getText().trim();
                customer = customerService.getByCusCode(cusCode);
                if (customer != null) {
                    txtFullName.setText(customer.getFullName());

                    txtAddress.setText(customer.getAddress());
                    txtPhoneNumber.setText(customer.getPhoneNumber());
                    txtEmail.setText(customer.getEmail());
                    rdoMale.setSelected(customer.getGender());
                    rdoFemale.setSelected(customer.getGender());

                    JOptionPane.showMessageDialog(this, "Found success");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Not found");
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

